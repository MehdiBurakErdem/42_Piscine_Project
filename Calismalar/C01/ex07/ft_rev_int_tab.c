void	ft_rev_int_tab(int *tab, int size)
{
	int	i;
	int	gci;

	i = 0;
	while (i < (size / 2))
	{
		gci = tab[i];
		tab[i] = tab[size - 1 - i];
		tab[size - 1 - i] = gci;
		i++;
	}
}
