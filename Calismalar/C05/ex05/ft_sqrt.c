int	ft_sqrt(int nb)
{
	int		sy;

	sy = 2;
	if (nb < 0)
		return (0);
	if (nb <= 1)
		return (nb);
	while (sy < 46341 && sy * sy <= nb)
	{
		if (sy * sy == nb)
			return (sy);
		sy++;
	}
	return (0);
}
