int	ft_str_is_alpha(char *str)
{
	char	gci;
	int		n;

	n = 0;
	while (str[n])
	{
		gci = str[n];
		if (!((gci <= 'z' && gci >= 'a') || (gci <= 'Z' && gci >= 'A')))
			return (0);
		n++;
	}
	return (1);
}
