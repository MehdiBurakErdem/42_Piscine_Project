int	ft_str_is_uppercase(char *str)
{
	char	gci;
	int		n;

	n = 0;
	while (str[n])
	{
		gci = str[n];
		if (!(gci <= 'Z' && gci >= 'A'))
			return (0);
		n++;
	}
	return (1);
}
