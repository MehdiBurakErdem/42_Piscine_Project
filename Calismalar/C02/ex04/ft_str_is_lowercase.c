int	ft_str_is_lowercase(char *str)
{
	char	gci;
	int		n;

	n = 0;
	while (str[n])
	{
		gci = str[n];
		if (!(gci <= 'z' && gci >= 'a'))
			return (0);
		n++;
	}
	return (1);
}
