int	ft_str_is_numeric(char *str)
{
	char	gci;
	int		n;

	n = 0;
	while (str[n])
	{
		gci = str[n];
		if (!((gci >= '0' && gci <= '9')))
			return (0);
		n++;
	}
	return (1);
}
